package bean;

public class Furniture {
    int furniture_id,detail_id,salevolume;
    String furniture_name,uid,type,pic_src;
    double price;
	public int getFurniture_id() {
		return furniture_id;
	}
	public void setFurniture_id(int furniture_id) {
		this.furniture_id = furniture_id;
	}
	public int getDetail_id() {
		return detail_id;
	}
	public void setDetail_id(int detail_id) {
		this.detail_id = detail_id;
	}
	public int getSalevolume() {
		return salevolume;
	}
	public void setSalevolume(int salevolume) {
		this.salevolume = salevolume;
	}
	public String getFurniture_name() {
		return furniture_name;
	}
	public void setFurniture_name(String furniture_name) {
		this.furniture_name = furniture_name;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getPic_src() {
		return pic_src;
	}
	public void setPic_src(String pic_src) {
		this.pic_src = pic_src;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
    
    
}
