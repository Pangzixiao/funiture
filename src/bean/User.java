package bean;

public class User {
    String uid,pass;
    int default_num;
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public int getDefault_num() {
		return default_num;
	}
	public void setDefault_num(int default_num) {
		this.default_num = default_num;
	}
    
}
